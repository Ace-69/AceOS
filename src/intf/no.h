char toUpperCase(char t){
    if(t != 'E' && t != 'B' && t != '\n' && t != 'L' && t != 'R' && t != 'C'){
        if((int)t >= 49 && (int) t <= 57) return (char)((int)t - 16);
        if((int)t >= 97 && (int)t <= 122) return (char)((int)t - 32);
        if(t == '0') return '=';
        if(t == '<') return '>';
        if(t == ',') return ';';
        if(t == '.') return ':';
        if(t == '-') return '_';
        if(t == '\\') return '|';
        if(t == '\'') return '?';
        if(t == '+') return '*';
    }
}